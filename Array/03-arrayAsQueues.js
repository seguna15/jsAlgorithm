/* 
* a queue is one of the most prominent use of arrays.
* Queue is FIFO (First-In-First-Out)
* It supports two operations
* Push - Append elements at the end
* Shift - gets an element from the begining of the array
 */


// demonstration of .push method:
let arr = [];
 arr.push('Hello World!');
 console.log(arr); // ['Hello World]

// demonstration of .shift method:
let arr1 = [0, 1, 2, 3];
arr1.shift();
console.log(arr1); // prints [1,2,3]


