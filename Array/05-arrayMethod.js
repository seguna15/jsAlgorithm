/* 
* Methods that work with the beginning of an Array
* 1) shift - extracts the first element
* 2) unshift - adds an element at the beginning of an array
*/

let names = ['Ajennay', 'Segun', 'John', 'Mathew', 'Ishan'];
names.shift();
console.log(names);

names.unshift('Noriega');
console.log(names);